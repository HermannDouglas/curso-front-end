// Declaração de variáveis
var nome = "João";
var idade = 25;
var altura = 1.75;
var estudante = true;

// Exibindo os valores das variáveis no console
console.log("Nome: " + nome);
console.log("Idade: " + idade);
console.log("Altura: " + altura);
console.log("Estudante: " + estudante);

// Realizando operações com as variáveis
var idadeDaquiACincoAnos = idade + 5;
var alturaEmMetros = altura * 100;

// Exibindo os resultados das operações no console
console.log("Daqui a cinco anos, a idade será: " + idadeDaquiACincoAnos);
console.log("Altura em centímetros: " + alturaEmMetros);


